package uebung1;

public class EdoAufg201 {

	static int add(int a, int b) { // "add" als variable für (int a, int b)

		return a + b; // "return" beschreibt die Bechenoperation
	}

	public static void main(String[] args) {

		System.out.println("Addiere 2 + 4: " + add(2, 4));
		System.out.println("Addiere 5 + 4: " + add(5, 4));

	}

}
